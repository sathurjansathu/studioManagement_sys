    public ArrayList<Finance5SupplierpayBean>retrieveData(){
        
        ArrayList<Finance5SupplierpayBean> al=null;
        
        al=new ArrayList<Finance5SupplierpayBean>();
        
        try{
            Connection conn=MySqlConnection();
            String qry="select * from financesupplier";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(qry);
            Finance5SupplierpayBean cus;
        
            while(rs.next()){
            
            cus=new Finance5SupplierpayBean(rs.getString("Suppliername"),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6));
             al.add(cus);
        }  
        }
        
        
        catch(Exception e){
            System.out.println(e);
            
        }
        
        return al;
        
    }
    
    public void fillTable(){
        
        ArrayList<Finance5SupplierpayBean> al=retrieveData();
        DefaultTableModel model=(DefaultTableModel)jTableFinance.getModel();
        model.setRowCount(0);
        Object[]row=new Object[6];
                
                for(int i=0; i<al.size(); i++){
                    
                    row[0]=al.get(i).getSuppliername();
                    row[1]=al.get(i).getSupplierid();
                    row[2]=al.get(i).getPrice();
                    row[3]=al.get(i).getWarranty();
                    row[4]=al.get(i).getNoOfQuantity();
                    row[5]=al.get(i).getTotalAmount();
                    
                    model.addRow(row);
                }
        
        
    }
    

