    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        
          
        if((cb_supplier!= null|| txtsupplierid !=null || textprice!=null || textwarranty !=null || 
                  textMinQty !=null || texttotal !=null  )){
            
            try
                  {
                
                       Connection conn = MySqlConnection();
                       String qry="insert into financesupplier values (?,?,?,?,?,?)";
                
                      PreparedStatement ps=conn.prepareStatement(qry);
                     
                      
                   
                     ps.setString(1,cb_supplier.getSelectedItem().toString());
                     ps.setInt(2, Integer.parseInt(txtsupplierid.getText()));
                      ps.setInt(3, Integer.parseInt(textprice.getText()));
                     ps.setInt(4, Integer.parseInt(textMinQty.getText()));
                     ps.setInt(5, Integer.parseInt( textwarranty.getText()));
                     ps.setInt(6, Integer.parseInt( texttotal.getText()));
                     
                     int res=ps.executeUpdate();
                
                     if(res>=1)
                     {
                    
                         JOptionPane.showMessageDialog(null,res+"number of task inserted ");
                     }
                    else
                         
                        JOptionPane.showMessageDialog(null,"insertion fail "); 
                    }
            
                   catch (Exception e) 
                   {
                    
                       JOptionPane.showMessageDialog(null,e);
            
                  }
          }  
        
        
    }//GEN-LAST:event_addbtnActionPerformed

