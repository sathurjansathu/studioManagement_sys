    private void cb_supplier1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_supplier1ActionPerformed

        try {
            String sName=cb_supplier1.getSelectedItem().toString();

            Connection conn=MySqlConnection();
            String query="SELECT * FROM salary WHERE name='"+sName+"'";
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while(rs.next()){
                Integer sNum=rs.getInt("eid");
                txtsupplierid1.setText(sNum.toString());
                Integer sNum2=rs.getInt("BasicSalary");
                txtsupplierid2.setText(sNum2.toString());
                Integer sNum3=rs.getInt("Overtime");
                txtsupplierid3.setText(sNum3.toString());
                Integer sNum4=rs.getInt("WorkingDays");
                txtsupplierid4.setText(sNum4.toString());
                Integer sNum5=rs.getInt("RatePerHours");
                txtsupplierid5.setText(sNum5.toString());
                Integer sNum6=rs.getInt("Bonus");
                txtsupplierid6.setText(sNum6.toString());
                Integer Total=(sNum2+(sNum3*sNum5)+sNum6);
                txtsupplierid7.setText(Total.toString());
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Finance2EmpSAL.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cb_supplier1ActionPerformed

