     public ArrayList<Finance2EmpSALbean>retrieveData(){
        
        ArrayList<Finance2EmpSALbean> al=null;
        
        al=new ArrayList<Finance2EmpSALbean>();
        
        try{
            Connection conn=MySqlConnection();
            String qry="select * from financesalary";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(qry);
            Finance2EmpSALbean cus;
        
            while(rs.next()){
            
            cus=new Finance2EmpSALbean(rs.getString("Name"),rs.getInt("Eid"),rs.getString("BasicSalary"),rs.getString("OverTime"),rs.getString("WorkingDays"),rs.getString("RateperHours"),rs.getString("Bonus"),rs.getString("Total"));
             al.add(cus);
        }  
        }
        
        
        catch(Exception e){
            System.out.println(e);
            
        }
        
        return al;
        
    }
    
    public void fillTable(){
        
        ArrayList<Finance2EmpSALbean> al=retrieveData();
        DefaultTableModel model=(DefaultTableModel)jTableEmp.getModel();
        model.setRowCount(0);
        Object[]row=new Object[8];
                
                for(int i=0; i<al.size(); i++){
                    
                    row[0]=al.get(i).getName();
                    row[1]=al.get(i).getEid();
                    row[2]=al.get(i).getBasicSalary();
                    row[3]=al.get(i).getOverTime();
                    row[4]=al.get(i).getWorkingDays();
                    row[5]=al.get(i).getRatePerHours();
                    row[6]=al.get(i).getBonus();
                    row[7]=al.get(i).getTotal();
                    model.addRow(row);
                }
        
        
    }
     public void loadSupplier(){
    
        try {
            Connection conn=MySqlConnection();
            
            String query="SELECT * FROM salary";
            PreparedStatement ps=conn.prepareStatement(query);
            ResultSet rs=ps.executeQuery();
            
            while(rs.next()){
                String SupplierName=rs.getString("name");
                cb_supplier1.addItem(SupplierName);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null,ex);
        }
    }

