                     ps.setInt(5, Integer.parseInt(txtsupplierid4.getText()));
                     ps.setInt(6, Integer.parseInt(txtsupplierid5.getText()));
                     ps.setInt(7, Integer.parseInt(txtsupplierid6.getText()));
