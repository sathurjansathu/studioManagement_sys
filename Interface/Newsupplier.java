      public void AutoID(){
        
       Connection conn=MySqlConnection();
         Statement st;
         
        try {
             txt_id.setText("Su001");
            st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(supplierid) FROM newsupplier");
            rs.next();
            rs.getString("MAX(supplierid)");
            
            if(rs.getString("MAX(supplierid)") == null){
                txt_id.setText("Su001");
            }else{
                long id = Long.parseLong(rs.getString("MAX(supplierid)").substring(2,rs.getString("MAX(supplierid)").length()));
                id++;
                txt_id.setText("Su" + String.format("%03d", id));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Newsupplier.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Error"+ex);
        }
    }
    
      
          public ArrayList<SupplierClass> getRepair_ItemsList()
    {
            ArrayList<SupplierClass> al = new ArrayList<SupplierClass>();
            Connection conn=MySqlConnection();
            String query = "SELECT * FROM newsupplier";
            
            Statement st;
            ResultSet rs;
            
        try{
            
            
             st = conn.createStatement();
            rs = st.executeQuery(query);
           SupplierClass cus;
        
            while(rs.next()){
            
            cus=new SupplierClass(rs.getString("spplierid"),rs.getString(2),rs.getString(3),rs.getString(4),rs.getInt(5));
             al.add(cus);
        }  
        }
        
        
        catch(Exception e){
            System.out.println(e);
            
        }
        
        return al;
        
    }
    
    public void fillTable(){
        
        ArrayList<SupplierClass> al=getRepair_ItemsList();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        Object[]row=new Object[5];
                
                for(int i=0; i<al.size(); i++){
                    
                     row[0]=al.get(i).getSupplierid();
                    row[1]=al.get(i).getCompanyname();
                    row[2]=al.get(i).getAddress();
                    row[3]=al.get(i).getEmail();
                    row[4]=al.get(i).getContactno();
                                 
                    model.addRow(row);
                }
        
        
    }
    

