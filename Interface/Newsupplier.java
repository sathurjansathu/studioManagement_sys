    private void ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDActionPerformed
        // TODO add your handling code here:
       
         
        if((txt_id!=null || txtcompanyname!=null || txtaddress!=null || 
                  txtemail!=null ||
                  txtcontactno!=null  )){
            
            try
                  {
                
                       Connection conn = MySqlConnection();
                       String qry="insert into newsupplier values (?,?,?,?,?)";
                
                      PreparedStatement ps=conn.prepareStatement(qry);
                     
                      
                     ps.setString(1,txt_id.getText());
                     ps.setString(2,txtcompanyname.getText());
                     ps.setString(3, txtaddress.getText());
                     ps.setString(4, txtemail.getText());
                    ps.setInt(5,Integer.parseInt(txtcontactno.getText()));
                
                    int res=ps.executeUpdate();
                      AutoID();
                        fillTable();
                     
                     if(res>=1)
                     {
                    
                         JOptionPane.showMessageDialog(null,res+"number of task inserted ");
                       
                     }
                    else
                         
                        JOptionPane.showMessageDialog(null,"insertion fail "); 
                    }
            
                   catch (Exception e) 
                   {
                    
                       JOptionPane.showMessageDialog(null,e);
            
                  }
          }
        
        
    }//GEN-LAST:event_ADDActionPerformed

